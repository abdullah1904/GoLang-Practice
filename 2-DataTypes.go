package main

import "fmt"

func main() {
	// String
	fmt.Printf("The value is %v and the type is %T\n", "Abdullah", "Abdullah")
	// Interger
	fmt.Printf("The value is %v and the type is %T\n", 10, 10)
	// Float
	fmt.Printf("The value is %v and the type is %T\n", 10.3, 10.3)
	// Boolean
	fmt.Printf("The value is %v and the type is %T\n", true, true)

}
